#include "../include/minitalk.h"

void	handler(int signal)
{
	static int	i;
	static char	c;

	if (signal == SIGUSR2)
		c += (1 << i);
	i++;
	if (i == 8)
	{
		ft_printf("%c", c);
		i = 0;
		c = 0;
	}
}
